using System;

class FloatOrDouble
{
    static void Main()
    {
        double a = 34.567839023;
        double b = 12.345;
        double c = 8923.1234857;
        double d = 3456.091;
        Console.WriteLine(a);
        Console.WriteLine(b);
        Console.WriteLine(c);
        Console.WriteLine(d);
        float e = 12.345f;
        Console.WriteLine(e);
        float f = 3456.091f;
        Console.WriteLine(f);
    }
}

